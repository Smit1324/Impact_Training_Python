# AVL TREES

# AVL Trees are defined as self balancing binary search tree, where the difference between heights of the left and the right subtress cannot be more than one for all nodes. The difference between left and right subtree for any node is known as balance factor of the node.

# Various operations on AVL Tree:
#     Insertion
#     Deletion
#     Traversal
#     Searching
# Searching will be similar to search operation in binary search tree.

# The various rotations on AVL Tree:
#     Left Rotation
#     Right Rotation

# When a node is added into the right subtree of a right subtree, if the tree gets out of balance then left rotation is performed.
# When a node is added into the left subtree of a left subtree, if the tree gets out of balance then right rotation is performed.

# When a node is added into the left subtree of a right subtree, if the tree gets out of balance then left-right rotation is performed.
# When a node is added into the right subtree of a left subtree, if the tree gets out of balance then right-left rotation is performed.

# Applications of AVL Trees:
#     It is used to index large records in database.
#     It is used in the implementation of balanced trees.
#     It is used in the implementation of priority queues.
#     It is used in the implementation of network routing.
#     It is used in the implementation of memory management.

# Disadvantages of AVL Trees:
#     It is difficult to implement.
#     It is difficult to maintain.
#     It is difficult to understand.

# Less used compared to Red-Black Trees.
# Due to its rather strict balance, AVL trees provide complicated insertion-removal operations as more rotations are performed.